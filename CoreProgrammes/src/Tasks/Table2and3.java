package Tasks;

public class Table2and3 {
    public static void main(String[] args) {
        System.out.println("------'2 Table'------");
        for(int i=1;i<=10;i++){
            System.out.println("2 * "+i+" = "+i*2);
        }
        System.out.println("---------------------");
        System.out.println("------'3 Table'------");
        for (int j=1;j<=10;j++){
            System.out.println("3 * "+j+" = "+j*3);
        }
        System.out.println("---------------------");
    }
}

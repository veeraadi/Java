package Operators;

public class Arithmetic {
    public static void main(String[] args) {
        int a=546;
        int b=6164;
        int add,sub,mul,div,mod;
        add=a+b;
        sub=b-a;
        mul=a*b;
        div=b/a;
        mod=b%a;
        System.out.println("Addition: "+add+"\nSubtraction: "+sub+"\nMultiplication: "+mul+"\nDivision: "+div+"\nModulo: "+mod);
    }
}
